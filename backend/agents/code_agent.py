def _lang_from_ext(ext: str) -> str:
    return {".py": "Python", ".js": "JavaScript", ".vue": "Vue", ".java": "Java", ".sql": "SQL"}.get(ext, ext)

def _extract_functions(content: str, ext: str) -> list:
    if ext == ".py":
        return re.findall(r"def (\w+)", content)[:20]
    elif ext in (".js", ".vue"):
        return re.findall(r"(?:function|const|let|async)\s+(\w+)", content)[:20]
    return []

def _extract_imports(content: str, ext: str) -> list:
    if ext == ".py":
        return re.findall(r"(?:from|import)\s+(\S+)", content)[:20]
    return []
